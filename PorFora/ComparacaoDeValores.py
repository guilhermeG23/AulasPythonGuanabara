#Comparar valores
a = 2
b = 1
#A maior que B
print(a > b)
#A maior ou igual a B
print(a >= b)
#B maior que A
print(a < b)
#B maior ou igual a A
print(a <= b)
#A igual a B
print(a == b)
#A diferente de B
print(a != b)
#A identifica que é True
print(a is True)
#B identifica que é True
print(b is True)
#A identifica que é False
print(a is False)
#B identifica que é False
print(b is False)
#A não é False
print(a is not False)
#B não é False
print(b is not False)
#A não é verdadeiro
print(a is not True)
#B não é verdadeiro
print(b is not True)
