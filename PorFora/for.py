#Imprimindo o range de um valor
print("\n\n")
for item in range(10):
    print(item)
#Fazendo o 10
print(item + 1)

#Valor unico
#Vai ate o valor
print("\n\n")
for i in range(5):
    print(i)

#Dois valores
#De ate onde
print("\n\n")
for i in range(3,6):
    print(i)

#Tres valores
#De ate onde com espaco entre os valores
print("\n\n") 
for i in range(4, 10, 2):
    print(i)        

#Ao contrario
print("\n\n")
for i in range(0, -10, -2):
    print(i)

#Valor reverso
print("\n\n")
for item in range(9, -1, -1):
    print(item)
print("\n\n")

