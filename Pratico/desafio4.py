#Filtrar tipo de entrada
entrada = input('Entrada: ')
print('Sua entrada é: {}'.format(entrada))
print('É Alpha-Numérica: {}'.format(entrada.isalpha()))
print('É Numérica: {}'.format(entrada.isnumeric()))
print('É Alpha: {}'.format(entrada.isalpha()))
print('É Decimal: {}'.format(entrada.isdecimal()))
print('É alpha: {}'.format(entrada.isalnum()))
